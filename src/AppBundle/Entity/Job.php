<?php

namespace AppBundle\Entity;

/**
 * Job
 */
class Job
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $titluJob;

    /**
     * @var string
     */
    private $descriere;

    /**
     * @var \DateTime
     */
    private $dataPublicarii;

    /**
     * @var boolean
     */
    private $esteActiv;

    /**
     * @var integer
     */
    private $orasId;

    /**
     * @var \AppBundle\Entity\Oras
     */
    private $oras;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set titluJob
     *
     * @param string $titluJob
     *
     * @return Job
     */
    public function setTitluJob($titluJob)
    {
        $this->titluJob = $titluJob;

        return $this;
    }

    /**
     * Get titluJob
     *
     * @return string
     */
    public function getTitluJob()
    {
        return $this->titluJob;
    }

    /**
     * Set descriere
     *
     * @param string $descriere
     *
     * @return Job
     */
    public function setDescriere($descriere)
    {
        $this->descriere = $descriere;

        return $this;
    }

    /**
     * Get descriere
     *
     * @return string
     */
    public function getDescriere()
    {
        return $this->descriere;
    }

    /**
     * Set dataPublicarii
     *
     * @param \DateTime $dataPublicarii
     *
     * @return Job
     */
    public function setDataPublicarii($dataPublicarii)
    {
        $this->dataPublicarii = $dataPublicarii;

        return $this;
    }

    /**
     * Get dataPublicarii
     *
     * @return \DateTime
     */
    public function getDataPublicarii()
    {
        return $this->dataPublicarii;
    }

    /**
     * Set esteActiv
     *
     * @param boolean $esteActiv
     *
     * @return Job
     */
    public function setEsteActiv($esteActiv)
    {
        $this->esteActiv = $esteActiv;

        return $this;
    }

    /**
     * Get esteActiv
     *
     * @return boolean
     */
    public function getEsteActiv()
    {
        return $this->esteActiv;
    }

    /**
     * Set orasId
     *
     * @param integer $orasId
     *
     * @return Job
     */
    public function setOrasId($orasId)
    {
        $this->orasId = $orasId;

        return $this;
    }

    /**
     * Get orasId
     *
     * @return integer
     */
    public function getOrasId()
    {
        return $this->orasId;
    }

    /**
     * Set oras
     *
     * @param \AppBundle\Entity\Oras $oras
     *
     * @return Job
     */
    public function setOras(\AppBundle\Entity\Oras $oras = null)
    {
        $this->oras = $oras;

        return $this;
    }

    /**
     * Get oras
     *
     * @return \AppBundle\Entity\Oras
     */
    public function getOras()
    {
        return $this->oras;
    }
}
